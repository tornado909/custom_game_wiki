// Typo corrections for function names as they appear in the dump
const dumpNameCorrections: Record<string, string> = {
  GetModofierPropertyPseudoRandomBonus: 'GetModifierPropertyPseudoRandomBonus',
  GEtModifierSlowResistance_Unique: 'GetModifierSlowResistance_Unique',
};

export function getEnumDescription(rawFunctionName?: string) {
  if (!rawFunctionName || rawFunctionName === 'Unused') return undefined;

  const functionName = dumpNameCorrections[rawFunctionName] ?? rawFunctionName;
  // eslint-disable-next-line @typescript-eslint/no-unnecessary-condition
  const description = modifiersData[functionName]?.[2];
  return description
    ? `${description}\n\nMethod Name: \`${functionName}\`.`
    : `Method Name: \`${functionName}\``;
}

export function modifierFunctionMethods(): apiTypes.ClassMethod[] {
  return serverDump
    .filter((x): x is DumpConstant => x.kind === 'constant')
    .filter((x): x is typeof x & { enum: string } => x.enum === 'modifierfunction')
    .filter((x): x is typeof x & { description: string } => x.description != null)
    .filter((x) => x.description !== 'Unused')
    .map((x): apiTypes.ClassMethod => {
      const rawName = x.description;
      const functionName = dumpNameCorrections[rawName] ?? rawName;

      // eslint-disable-next-line @typescript-eslint/no-unnecessary-condition
      const [argumentType, returns, description] = modifiersData[functionName] ?? [null, ['nil']];

      const isBroken = x.name in modifierProperties && modifierProperties[x.name] === false;

      // Keep the documented type (args + returns) even when the property is
      // currently "broken" — the type is documentation, and `broken` already
      // flags that it doesn't work in this build. Stripping the type to nil made
      // the method's signature flip every time the modifier test result changed,
      // producing phantom Lua API changelog churn. The broken/working transition
      // itself is tracked in the "Properties Fixed" category instead.
      const args: apiTypes.FunctionParameter[] = [];
      if (argumentType !== null) {
        args.push({ name: 'event', types: [argumentType] });
      }

      return {
        kind: 'function',
        name: functionName,
        available: 'both',
        abstract: true,
        description,
        args,
        returns,
        ...(isBroken ? { broken: true as const } : {}),
      };
    });
}

export function validateModifierMethod(method: apiTypes.ClassMethod) {
  const functionName = method.name;

  if (!(functionName in modifiersData)) {
    console.warn(`Untyped modifier field: ${functionName}`);
  }
}
